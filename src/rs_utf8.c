unsigned validate_utf8(
    void const * buf,
    size_t size,
    unsigned utf8_state
) {
    for (uint8_t const * p = buf, *p_over = p + size; p < p_over; p++) {
        utf8_state = utf8table[256 + 16 * utf8_state + utf8table[*p]];
        if (!RS_IS_VALID_UTF8_FRAGMENT(utf8_state)) {
            break;
        }
    }
    return utf8_state;
}
